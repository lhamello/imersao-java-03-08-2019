package br.com.tt.aula01;

// 1) Faça um programa que imprima a soma de dois números.
// Parâmetros
// - Valor a: 137
// - Valor b: 145
public class Exercicio01 {

	public static void main(String[] args) {
		int valorA = 137;
		int valorB = 145;
		System.out.println(valorA + " + " + valorB + " = " + (valorA + valorB));
	}
}
