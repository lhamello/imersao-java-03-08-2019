package br.com.tt.aula01;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		int a = 10;
		int b = 10;
		
		System.out.println(a == b);
		System.out.println(a != b);
		
		b = 11;
		System.out.println(a > b);
		System.out.println(a < b);
		
		System.out.println(a <= b);
	}
}
